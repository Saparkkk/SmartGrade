    if len(records) < 2:
        return None  # ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏°‡πà‡∏û‡∏≠‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö

